def test_build_requires_n_mol_or_weight_fraction(tmp_path, monkeypatch):
    cfg = BuildConfig(
        components=[
            ComponentSpec(smiles="C", name="methane"),  # no n_mol, no wf
        ],
        output_dir=str(tmp_path / "bad"),
    )

    def _prep(smiles="", sdf_path="", pdb_path="", name="MOL"):
        return _FakeMol(name=name)

    monkeypatch.setattr(
        "abmptools.amorphous.builder.prepare_molecule", _prep)
    monkeypatch.setattr(
        "abmptools.amorphous.builder.get_molecular_weight", lambda m: 16.0)
    monkeypatch.setattr(
        "abmptools.amorphous.builder.write_single_mol_pdb",
        lambda m, p: Path(p).write_text("fake\n") or str(p))

    builder = AmorphousBuilder(cfg)
    with pytest.raises(ValueError):
        builder.build()
