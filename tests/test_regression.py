def _ajf_id(params):
    """Create a short test ID from generateajf params."""
    return " ".join(params)


@pytest.mark.parametrize("extra_args", GENERATEAJF_PARAMS, ids=_ajf_id)
def test_generateajf_gly5(tmp_path, extra_args):
    """generateajf output must match main-branch reference."""
    pdb_src = os.path.join(SAMPLE_DIR, "generateajf", "gly5.pdb")
    shutil.copy(pdb_src, tmp_path / "gly5.pdb")

    result = _run(
        ["abmptools.generateajf", "-i", "gly5.pdb"] + list(extra_args),
        cwd=str(tmp_path),
    )
    assert result.returncode == 0, f"generateajf failed:\n{result.stderr}"

    # Find the generated .ajf file
    ajf_files = [f for f in os.listdir(tmp_path) if f.endswith(".ajf")]
    assert len(ajf_files) == 1, f"Expected 1 ajf, got {ajf_files}"
    ajf_name = ajf_files[0]

    ref_path = os.path.join(REF_MAIN, "generateajf_gly5", ajf_name)
    assert os.path.exists(ref_path), f"No reference file: {ref_path}"
    _compare_files(str(tmp_path / ajf_name), ref_path)


