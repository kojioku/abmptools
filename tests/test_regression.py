"""Regression tests: verify CLI output matches reference values.

Reference files were generated from:
- main branch (pre-refactor): generateajf, log2cpf, convertcpf
- pre-refactor commit (7cd75bf): udf2gro, gro2udf (udf+gro mode, from-top mode)
"""
import os
import re
import shutil
import subprocess
import sys

import pytest

TESTS_DIR = os.path.dirname(__file__)
REF_MAIN = os.path.join(TESTS_DIR, "regression", "reference", "main")
REF_PREREFACTOR = os.path.join(TESTS_DIR, "regression", "reference", "prerefactor")
SAMPLE_DIR = os.path.join(TESTS_DIR, os.pardir, "sample")


def _run(args, cwd):
    """Run a CLI command and return completed process."""
    return subprocess.run(
        [sys.executable, "-m"] + args,
        cwd=cwd,
        capture_output=True,
        text=True,
        timeout=120,
    )


def _normalize_line(line):
    """Normalize lines that contain timestamps or absolute paths."""
    # CPF header: "... Generated by ABMPTools [log2cpf] 2026-04-05 00:54:50.125453"
    line = re.sub(
        r"(Generated by ABMPTools\b.*?)\s+\d{4}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2}\.\d+",
        r"\1 <TIMESTAMP>",
        line,
    )
    # TOP header: "; input: /tmp/.../test.udf"
    line = re.sub(r"^(;\s*input:\s+).*$", r"\1<PATH>", line)
    return line


def _compare_files(generated, reference, ignore_blank_lines=False):
    """Assert two files have identical content (ignoring timestamps/paths)."""
    with open(generated) as f:
        gen_lines = f.readlines()
    with open(reference) as f:
        ref_lines = f.readlines()
    if ignore_blank_lines:
        gen_lines = [l for l in gen_lines if l.strip()]
        ref_lines = [l for l in ref_lines if l.strip()]
    gen_text = "".join(_normalize_line(l) for l in gen_lines)
    ref_text = "".join(_normalize_line(l) for l in ref_lines)
    assert gen_text == ref_text, (
        f"Output differs from reference.\n"
        f"  generated: {generated}\n"
        f"  reference: {reference}\n"
    )


