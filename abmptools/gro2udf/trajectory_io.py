# -*- coding: utf-8 -*-
"""
trajectory_io.py
----------------
Readers for GROMACS multi-frame .gro and .xvg files, used by the
``--trajectory`` and ``--energy`` CLI options of ``abmptools.gro2udf``.

Both readers are written in pure Python (stdlib only) so they work without
MDAnalysis being installed.
"""
from __future__ import annotations

import os
import re
from typing import Dict, List, Tuple

from .top_model import GROFrameData


_GRO_TITLE_RE = re.compile(
    r"t\s*=\s*([\d.eE+\-]+)\s*(?:step\s*=\s*(\d+))?",
    re.IGNORECASE,
)


def frames_from_multi_gro(path: str) -> List[GROFrameData]:
    """Parse a multi-frame GROMACS .gro file.

    Each frame in a GROMACS .gro stream is:

    .. code-block:: text

        <title line>            # e.g. "Generated by trjconv : t= 0.0 step= 0"
        <natom>                 # integer
        <natom atom lines>      # fixed-column: resID/resname/atomname/atomID + x y z [+ vx vy vz]
        <box line>              # 3 floats (xx yy zz) or 9 (full tensor)

    A multi-frame file is simply this block repeated. ``gmx trjconv -o
    something.gro`` writes exactly this format.

    Coordinates are interpreted with fixed columns (chars 20..28 / 28..36 /
    36..44) to remain robust against name fields that contain spaces (the
    ``parts.split()`` heuristic breaks when the atom name field is short).

    Time / step are taken from the title line via the ``t=`` / ``step=``
    tokens that ``gmx trjconv`` emits. When absent (e.g. hand-written .gro),
    they default to 0.0 / 0.

    Returns
    -------
    list of GROFrameData
        One entry per frame in file order. Coordinates are nm (the gro
        native unit); cell is the [xx, yy, zz] diagonal in nm.

    Raises
    ------
    FileNotFoundError
        When ``path`` does not exist.
    ValueError
        When the file is empty or a frame's atom count line cannot be parsed.
    """
    if not os.path.exists(path):
        raise FileNotFoundError(f"multi-frame .gro not found: {path}")

    frames: List[GROFrameData] = []
    with open(path) as f:
        while True:
            title = f.readline()
            if not title:
                break              # EOF
            title_stripped = title.rstrip()
            natom_line = f.readline()
            if not natom_line:
                break              # malformed trailing frame
            try:
                natom = int(natom_line.strip())
            except ValueError as exc:
                raise ValueError(
                    f"multi-frame .gro {path}: expected atom-count integer, "
                    f"got {natom_line!r}"
                ) from exc

            coords: List[List[float]] = []
            for _ in range(natom):
                line = f.readline()
                if not line:
                    raise ValueError(
                        f"multi-frame .gro {path}: EOF inside frame "
                        f"(expected {natom} atom lines)"
                    )
                # Fixed-column reading: chars 20..44 for x/y/z (1-based 21..44).
                # https://manual.gromacs.org/current/reference-manual/file-formats.html#gro
                try:
                    x = float(line[20:28])
                    y = float(line[28:36])
                    z = float(line[36:44])
                except ValueError as exc:
                    raise ValueError(
                        f"multi-frame .gro {path}: bad coordinate line {line!r}"
                    ) from exc
                coords.append([x, y, z])

            box_line = f.readline()
            if not box_line:
                raise ValueError(
                    f"multi-frame .gro {path}: missing box line for frame "
                    f"after {len(frames)} complete frames"
                )
            box_parts = box_line.split()
            if len(box_parts) < 3:
                raise ValueError(
                    f"multi-frame .gro {path}: box line has <3 fields: "
                    f"{box_line!r}"
                )
            cell = [float(box_parts[0]), float(box_parts[1]), float(box_parts[2])]

            time = 0.0
            step = 0
            m = _GRO_TITLE_RE.search(title_stripped)
            if m:
                time = float(m.group(1))
                if m.group(2):
                    step = int(m.group(2))

            frames.append(GROFrameData(
                step=step, time=time,
                coord_list=coords, cell=cell,
            ))

    if not frames:
        raise ValueError(f"multi-frame .gro {path}: no frames found")
    return frames


def read_xvg(path: str) -> Tuple[List[float], Dict[str, List[float]]]:
    """Parse a GROMACS-style .xvg file (e.g. output of ``gmx energy``).

    Reads:

    - ``@ s<N> legend "<name>"`` lines into a list of column legends.
    - Data lines (no leading ``@``/``#``) as space-separated floats.
      Column 0 is the time (or x-axis value); the remaining columns are
      named according to the legend in order.

    Parameters
    ----------
    path : str
        Path to the .xvg file.

    Returns
    -------
    times : list of float
        Time (or x-axis) values, one entry per data row.
    series : dict mapping legend name -> list of float
        Energy / property time series. Order follows ``@ sN legend ...``
        declarations. ``len(series[name]) == len(times)``.

    Raises
    ------
    FileNotFoundError
        When ``path`` does not exist.
    ValueError
        When no legend lines are found or a data row has fewer columns
        than expected.
    """
    if not os.path.exists(path):
        raise FileNotFoundError(f".xvg file not found: {path}")

    legend_re = re.compile(r'^@\s+s(\d+)\s+legend\s+"([^"]+)"')
    legends_by_index: Dict[int, str] = {}
    times: List[float] = []
    columns: List[List[float]] = []

    with open(path) as f:
        for raw_line in f:
            line = raw_line.strip()
            if not line:
                continue
            if line.startswith("#"):
                continue
            if line.startswith("@"):
                m = legend_re.match(line)
                if m:
                    legends_by_index[int(m.group(1))] = m.group(2)
                continue
            # data row
            parts = line.split()
            try:
                values = [float(p) for p in parts]
            except ValueError:
                # Non-numeric data row — skip silently (some xvg files
                # have stray annotation lines).
                continue
            if not values:
                continue
            times.append(values[0])
            data_cols = values[1:]
            while len(columns) < len(data_cols):
                # Pad with empty list aligned to current row count
                columns.append([0.0] * (len(times) - 1))
            for i, v in enumerate(data_cols):
                columns[i].append(v)
            # Pad shorter columns to current length
            for i in range(len(data_cols), len(columns)):
                columns[i].append(0.0)

    if not legends_by_index:
        raise ValueError(
            f".xvg file {path}: no `@ sN legend \"...\"` lines found"
        )
    if not times:
        raise ValueError(f".xvg file {path}: no data rows found")

    # Map column index -> legend name; fill gaps with synthetic names.
    series: Dict[str, List[float]] = {}
    for col_idx, col_values in enumerate(columns):
        name = legends_by_index.get(col_idx, f"col{col_idx}")
        series[name] = col_values
    return times, series
